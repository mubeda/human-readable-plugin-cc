#!/usr/bin/env python3
"""
human-readable: Generate a single self-contained HTML viewer from a folder of Markdown files.

Usage:
    python generate.py                      # use .human-readable.yml; first run is interactive
    python generate.py path/to/docs         # one-off override of source path
    python generate.py --watch              # rebuild on MD/image change (needs: pip install watchdog)
    python generate.py --help               # show this message
"""
from __future__ import annotations

